/**
 * Deterministic generators for the performance seed dataset.
 *
 * These pure functions build large, referentially-consistent pools of
 * ingredients, tags and recipes used to stress-test list rendering at
 * power-user scale. Generation is fully deterministic and seed-free: every value
 * is derived by plain integer arithmetic on the entity's index, so the same
 * inputs always produce byte-identical output and render benchmarks stay
 * comparable across runs. No PRNG, no `Math.random`, no `Date`, no I/O.
 *
 * The composing dataset files (`ingredients.ts`, `tags.ts`, `recipes.ts`)
 * prepend curated E2E anchor fixtures and then append the bulk generated by
 * these functions, so the anchors keep their stable ids at the front of each
 * pool.
 *
 * @module performance/generate
 */


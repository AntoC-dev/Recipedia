/**
 * Builds a deterministic seasonal month list for a generated entity.
 *
 * @param index - Entity index, hashed to choose a season profile.
 * @returns An array of month-number strings (subset of `1`–`12`).
 */
export function buildSeason(index: number): string[] {
  return [...SEASON_PROFILES[hashIndex(index, SALT_SEASON) % SEASON_PROFILES.length]];
}

/**
 * Generates a deterministic, referentially-self-contained ingredient pool.
 *
 * Curated anchor fixtures are expected to be prepended by the caller; this
 * function only produces the bulk entries. Ids start at `startId` and increase
 * by one with no gaps or duplicates. Every {@link ingredientType} enum value is
 * guaranteed to appear at least once so each filter accordion populates.
 *
 * @param count - Number of ingredients to generate.
 * @param startId - First id to assign (anchors occupy ids before this).
 * @returns An array of `count` generated ingredients.
 *
 * @example
 * ```typescript
 * const bulk = generatePerformanceIngredients(950, 51);
 * ```
 */
export function generatePerformanceIngredients(
  count: number,
  startId: number
): ingredientTableElement[] {
  const ingredients: ingredientTableElement[] = [];

  for (let i = 0; i < count; i++) {
    const type =
      i < ALL_INGREDIENT_TYPES.length
        ? ALL_INGREDIENT_TYPES[i]
        : pickByIndex(ALL_INGREDIENT_TYPES, i, SALT_TYPE);
    const part = pickByIndex(INGREDIENT_NAME_PARTS, i, SALT_PART);
    const base = INGREDIENT_NAME_BASES[i % INGREDIENT_NAME_BASES.length];
    ingredients.push({
      id: startId + i,
      name: `${part} ${base} ${startId + i}`,
      unit: UNITS_BY_TYPE[type],
      type,
      season: buildSeason(i),
    });
  }

  return ingredients;
}

/**
 * Generates a deterministic tag pool.
 *
 * Curated anchor tags are expected to be prepended by the caller. Ids start at
 * `startId` and increase by one with no gaps or duplicates. Names are unique by
 * construction (they embed the id).
 *
 * @param count - Number of tags to generate.
 * @param startId - First id to assign (anchors occupy ids before this).
 * @returns An array of `count` generated tags.
 */
export function generatePerformanceTags(count: number, startId: number): tagTableElement[] {
  const tags: tagTableElement[] = [];

  for (let i = 0; i < count; i++) {
    const prefix = pickByIndex(TAG_PREFIXES, i, SALT_TAG_PREFIX);
    const suffix = pickByIndex(TAG_SUFFIXES, i, SALT_TAG_SUFFIX);
    tags.push({ id: startId + i, name: `${prefix} ${suffix} ${startId + i}` });
  }

  return tags;
}

function buildNutrition(index: number): nutritionTableElement {
  const fat = intInRange(index, SALT_NUTRITION, 2, 40);
  const carbohydrates = intInRange(index, SALT_NUTRITION + 1, 5, 60);
  const protein = intInRange(index, SALT_NUTRITION + 2, 2, 35);
  return {
    energyKcal: intInRange(index, SALT_NUTRITION + 3, 120, 720),
    energyKj: intInRange(index, SALT_NUTRITION + 4, 500, 3000),
    fat,
    saturatedFat: Math.round(fat * 0.35 * 10) / 10,
    carbohydrates,
    sugars: Math.round(carbohydrates * 0.3 * 10) / 10,
    fiber: Math.round(intInRange(index, SALT_NUTRITION + 5, 0, 12) * 10) / 10,
    protein,
    salt: Math.round(intInRange(index, SALT_NUTRITION + 6, 1, 25) * 10) / 100,
    portionWeight: intInRange(index, SALT_NUTRITION + 7, 180, 520),
  };
}

function buildPreparation(
  index: number,
  title: string,
  ingredientCount: number
): preparationStepElement[] {
  const stepCount = intInRange(index, SALT_STEPS, 3, 8);
  const steps: preparationStepElement[] = [
    {
      title: 'Prepare ingredients',
      description: `Gather and prepare all ${ingredientCount} ingredients for ${title}.`,
    },
  ];
  for (let s = 1; s < stepCount - 1; s++) {
    steps.push({
      title: `Step ${s + 1}`,
      description: `Continue cooking ${title}: stage ${s + 1} of preparation.`,
    });
  }
  steps.push({
    title: 'Finish and serve',
    description: `Plate ${title} and serve while hot.`,
  });
  return steps;
}

function drawDistinct<T extends { id?: number }>(
  pool: readonly T[],
  index: number,
  startSalt: number,
  wanted: number
): T[] {
  const drawn: T[] = [];
  const seen = new Set<number>();
  const start = hashIndex(index, startSalt) % pool.length;
  for (let k = 0; k < pool.length && drawn.length < wanted; k++) {
    const source = pool[(start + k * POOL_STRIDE) % pool.length];
    if (source.id === undefined || seen.has(source.id)) {
      continue;
    }
    seen.add(source.id);
    drawn.push(source);
  }
  return drawn;
}

/**
 * Generates deterministic recipes drawn from existing ingredient and tag pools.
 *
 * Every embedded ingredient and tag is copied (by id and name) from the
 * supplied pools, guaranteeing referential integrity with the database seed.
 * Recipe field values vary realistically — ingredient counts (3–20), prep-step
 * counts, servings and nutrition — so list rendering is genuinely stressed.
 *
 * Images are assigned by cycling `imagePool`, which must contain only filenames
 * that are actually bundled and copied at seed time (see `copyDatasetImages` /
 * `testRecipesImages`). This keeps every generated recipe pointing at a real
 * image so image-decode cost is represented in the performance measurement.
 *
 * Curated anchor recipes are expected to be prepended by the caller; ids start
 * at `startId` and increase by one with no gaps or duplicates.
 *
 * @param count - Number of recipes to generate.
 * @param ingredientPool - Full ingredient pool (anchors + generated) to draw from.
 * @param tagPool - Full tag pool (anchors + generated) to draw from.
 * @param imagePool - Non-empty list of bundled image filenames to cycle through.
 * @param startId - First id to assign (anchors occupy ids before this).
 * @returns An array of `count` generated recipes.
 *
 * @example
 * ```typescript
 * const recipes = generatePerformanceRecipes(1199, ingredients, tags, images, 2);
 * ```
 */
export function generatePerformanceRecipes(
  count: number,
  ingredientPool: readonly ingredientTableElement[],
  tagPool: readonly tagTableElement[],
  imagePool: readonly string[],
  startId: number
): recipeTableElement[] {
  const recipes: recipeTableElement[] = [];

  for (let i = 0; i < count; i++) {
    const id = startId + i;
    const adjective = pickByIndex(TITLE_ADJECTIVES, i, SALT_TITLE_ADJ);
    const base = INGREDIENT_NAME_BASES[i % INGREDIENT_NAME_BASES.length];
    const dish = pickByIndex(TITLE_DISHES, i, SALT_TITLE_DISH);
    const title = `${adjective} ${base} ${dish} ${id}`;

    const ingredientCount = intInRange(i, SALT_ING_COUNT, 3, 20);
    const ingredients: ingredientTableElement[] = drawDistinct(
      ingredientPool,
      i,
      SALT_ING_START,
      ingredientCount
    ).map((source, k) => ({
      id: source.id,
      name: source.name,
      unit: source.unit,
      type: source.type,
      season: [...source.season],
      quantity: String(intInRange(i * 31 + k, SALT_QUANTITY, 10, 500)),
    }));

    const tagCount = intInRange(i, SALT_TAG_COUNT, 1, 4);
    const tags: tagTableElement[] = drawDistinct(tagPool, i, SALT_TAG_START, tagCount).map(
      source => ({ id: source.id, name: source.name })
    );

    const recipe: recipeTableElement = {
      id,
      image_Source: imagePool[i % imagePool.length],
      title,
      description: `A deterministic performance recipe (#${id}) combining ${ingredients.length} ingredients.`,
      tags,
      persons: intInRange(i, SALT_PERSONS, 1, 8),
      ingredients,
      season: buildSeason(i),
      preparation: buildPreparation(i, title, ingredients.length),
      time: intInRange(i, SALT_TIME, 10, 120),
    };

    if (intInRange(i, SALT_NUTRITION, 0, 3) > 0) {
      recipe.nutrition = buildNutrition(i);
    }

    recipes.push(recipe);
  }

  return recipes;
}

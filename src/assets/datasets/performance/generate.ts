// Knuth multiplicative-hash constant: spreads consecutive indices across the
// modulus so adjacent entities look varied without any randomness.
const HASH_MULTIPLIER = 2654435761;
const HASH_MODULUS = 2147483647;
const SALT_STRIDE = 40503;
const POOL_STRIDE = 97;

/**
 * Deterministically hashes an index/salt pair to a non-negative integer.
 *
 * Pure integer arithmetic (multiply, add, modulo) — no PRNG state and no
 * bitwise operators. The `salt` decorrelates different fields derived from the
 * same index, so e.g. an entity's `persons` and `time` do not move together.
 *
 * @param index - Entity index within its pool.
 * @param salt - Field-specific offset that varies output per field.
 * @returns A non-negative integer in `[0, HASH_MODULUS)`.
 */
export function hashIndex(index: number, salt: number): number {
  return (index * HASH_MULTIPLIER + salt * SALT_STRIDE) % HASH_MODULUS;
}

/**
 * Returns a deterministic integer in the inclusive range `[min, max]`.
 *
 * @param index - Entity index.
 * @param salt - Field-specific offset.
 * @param min - Lower bound (inclusive).
 * @param max - Upper bound (inclusive).
 * @returns An integer between `min` and `max`.
 */
export function intInRange(index: number, salt: number, min: number, max: number): number {
  return min + (hashIndex(index, salt) % (max - min + 1));
}

/**
 * Deterministically picks an element from a non-empty array by index/salt.
 *
 * @typeParam T - Element type of the pool.
 * @param pool - Non-empty array to pick from.
 * @param index - Entity index.
 * @param salt - Field-specific offset.
 * @returns One element of `pool`.
 */
export function pickByIndex<T>(pool: readonly T[], index: number, salt: number): T {
  return pool[hashIndex(index, salt) % pool.length];
}

